const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  powerOf: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(10, 2);
calculator.minus(10, 2);
calculator.divide(10, 2);
calculator.powerOf(10, 2);
